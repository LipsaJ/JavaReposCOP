
public class Primitive {
	
	public static void main(String[] args) {
		int myNumber ; //declare
		myNumber = 88; //initialize --32
		
		short myShort = 10; //declare and initialize at once --16
		long myLong = 9797; //64 really long numbers
		
		double myDouble = 56.7345; //mostly double is used how we use int
		float myFloat = 3.4f; //always with ends with a f, smaller than Double
		
		char myChar = 'a';
		boolean myBoolean = true;
		byte myByte = 127;	
		
		System.out.println(myNumber);
		System.out.println(myShort);
		System.out.println(myLong);
		System.out.println(myDouble);
		System.out.println(myFloat);
		System.out.println(myChar);
		System.out.println(myBoolean);
		System.out.println(myByte);
	}

}
